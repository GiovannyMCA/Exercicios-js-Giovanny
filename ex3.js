// Exercício 3: Ímpar ou par
// Faça um programa que receba um número do usuário e informe se este número é par ou
// ímpar.

let número = parseInt(prompt ("Digite um número e eu falarei se é par ou ímpar"));

if (número %2 == 0){
    alert ("Este número é par")
}
else {
    alert ("Este número é ímpar")
}